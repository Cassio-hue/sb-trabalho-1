SECTION TEXT

INICIO: 
    INPUT N1
    INPUT N2
    INPUT N3
    JMP COMP

CONTINUE: 
    INPUT USR
    LOAD USR
    SUB UM
    JMPZ INICIO
    STOP

COMP:
    LOAD N1
    SUB N2
    JMPP N1_MAIOR_N2
    JMPN N2_MAIOR_N1

N2_MAIOR_N1: 
    COPY N1, MENOR
    COPY N2, MAIOR
    LOAD N2
    SUB N3
    JMPN N3_MAIOR
    JMPP N3_MENOR

N1_MAIOR_N2: COPY N2, MENOR
    COPY N1, MAIOR
    LOAD N1
    SUB N3
    JMPN N3_MAIOR
    JMPP N3_MENOR

N3_MAIOR: 
    COPY N3, MAIOR
    JMP PRINT

N3_MENOR: LOAD MENOR
    SUB N3
    JMPN PRINT
    COPY N3, MENOR

PRINT: 
    OUTPUT MAIOR
    OUTPUT MENOR

JMP CONTINUE

SECTION DATA
N1: SPACE
N2: SPACE
N3: SPACE
USR: SPACE
UM: CONST 1
MAIOR: SPACE
MENOR: SPACE